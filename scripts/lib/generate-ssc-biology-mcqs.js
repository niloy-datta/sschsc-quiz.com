function generateBiologyMcq(chapterNo, setNo, qIndex) {
  const ch = String(chapterNo).padStart(2, "0");
  const seed = Number(chapterNo) * 10000 + setNo * 1000 + qIndex * 17;
  const factoryFn = CHAPTER_FACTORIES[ch] ?? CHAPTER_FACTORIES["01"];
  const raw = factoryFn(seed, qIndex);
  return withShuffledAnswer(raw, seed);
}

function toPublicQuestion(raw, chapterName) {
  const opts = [raw.o.ক, raw.o.খ, raw.o.গ, raw.o.ঘ];
  const answerIndex = LABELS.indexOf(raw.a);
  return {
    text: raw.q,
    options: opts,
    answerIndex: answerIndex >= 0 ? answerIndex : 0,
    explanation: `${chapterName} — ${raw.t}`,
    topic: raw.t ?? chapterName,
    image: null,
  };
}

function generateBiologySet(chapterNo, setNo) {
  const ch = String(chapterNo).padStart(2, "0");
  const chapterName = BIOLOGY_CHAPTER_NAMES[ch] ?? `অধ্যায় ${ch}`;
  const seen = new Set();
  const out = [];
  let attempt = 0;
  while (out.length < 25 && attempt < 500) {
    attempt++;
    const qIndex = out.length + 1 + attempt * 3 + setNo * 11;
    const raw = generateBiologyMcq(chapterNo, setNo, qIndex);
    const key = raw.q.replace(/\s+/g, " ").trim().toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(toPublicQuestion(raw, chapterName));
  }
  return out.slice(0, 25);
}

